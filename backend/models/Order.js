// backend/models/Order.js (CORRIGÉ)
const mongoose = require('mongoose');

const orderSchema = new mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  orderNumber: {
    type: String,
    unique: true
  },
  items: [{
    product: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'Product',
      required: true
    },
    name: String,
    quantity: {
      type: Number,
      required: true,
      min: 1
    },
    price: {
      type: Number,
      required: true
    },
    image: String
  }],
  shippingAddress: {
    name: { type: String, required: true },
    street: { type: String, required: true },
    city: { type: String, required: true },
    postalCode: { type: String, required: true },
    country: { type: String, required: true },
    phone: { type: String, required: true }
  },
  paymentMethod: {
    type: String,
    required: true,
    enum: ['card', 'paypal', 'cash_on_delivery']
  },
  paymentResult: {
    id: String,
    status: String,
    updateTime: String,
    emailAddress: String
  },
  itemsPrice: {
    type: Number,
    required: true,
    default: 0.0
  },
  shippingPrice: {
    type: Number,
    required: true,
    default: 0.0
  },
  taxPrice: {
    type: Number,
    required: true,
    default: 0.0
  },
  totalPrice: {
    type: Number,
    required: true,
    default: 0.0
  },
  isPaid: {
    type: Boolean,
    required: true,
    default: false
  },
  paidAt: Date,
  isDelivered: {
    type: Boolean,
    required: true,
    default: false
  },
  deliveredAt: Date,
  // ➡️ MODIFIÉ : Utilisation de la casse "Title Case" pour correspondre au Controller et au Frontend
  status: {
    type: String,
    required: true,
    // Note : J'ai inclus 'Processing' car il était dans votre ancien enum, mais pas dans notre nouvelle liste Frontend/Controller.
    // Pour une harmonisation parfaite avec nos dernières étapes, nous allons coller aux 4 statuts définis.
    enum: ['Pending', 'Shipped', 'Delivered', 'Cancelled'], 
    default: 'Pending'
  }
}, {
  timestamps: true
});

// Générer un numéro de commande unique (SANS next callback)
orderSchema.pre('save', function() {
  if (!this.orderNumber) {
    const date = new Date();
    const year = date.getFullYear();
    const month = String(date.getMonth() + 1).padStart(2, '0');
    const random = Math.floor(Math.random() * 10000).toString().padStart(4, '0');
    this.orderNumber = `CMD-${year}${month}-${random}`;
  }
});

module.exports = mongoose.model('Order', orderSchema);